def gen_assets(n=30):
    rows = []
    for i in range(1, n + 1):
        atype = random.choice(ASSET_TYPES)
        mfr   = random.choice(MANUFACTURERS[atype])
        model = random.choice(MODELS[atype])
        rows.append((
            f"AST-{i:04d}",
            f"{mfr} {model} #{i:03d}",
            atype,
            random.choice(LOCATIONS),
            mfr,
            model,
            random.randint(2010, 2022),
            random.choices(["Active", "Inactive", "Under Maintenance"], weights=[70, 10, 20])[0],
        ))
    return rows

def gen_technicians(n=20):
    rows = []
    for i in range(1, n + 1):
        level = random.choice(SKILL_LEVELS)
        rows.append((
            f"TECH-{i:04d}",
            fake.name(),
            level,
            random.choice(SPECIALISATIONS),
            HOURLY_RATES[level] + random.uniform(-5, 10),
            1,
        ))
    return rows

def gen_work_orders(assets, technicians, n=500):
    rows = []
    base_date = datetime(2023, 1, 1)
    asset_ids = [a[0] for a in assets]
    tech_ids  = [t[0] for t in technicians]

    for i in range(1, n + 1):
        wo_type  = random.choices(WO_TYPES, weights=[35, 35, 20, 10])[0]
        priority = random.choices(PRIORITIES, weights=[10, 25, 40, 25])[0]
        status   = random.choices(STATUSES, weights=[10, 15, 70, 5])[0]

        created_at = base_date + timedelta(
            days=random.randint(0, 364),
            hours=random.randint(6, 20)
        )

        started_at = completed_at = None
        downtime_hours = None

        if status in ("In Progress", "Completed", "Cancelled"):
            started_at = created_at + timedelta(hours=random.uniform(0.5, 8))

        if status == "Completed":
            target_h = SLA_TARGETS[priority][0]
            # ~75% of WOs meet SLA
            if random.random() < 0.75:
                duration = random.uniform(target_h * 0.2, target_h * 0.95)
            else:
                duration = random.uniform(target_h * 1.0, target_h * 2.5)
            completed_at = started_at + timedelta(hours=duration)
            downtime_hours = round(random.uniform(0, duration * 0.8), 2)

        rows.append((
            f"WO-{i:05d}",
            random.choice(asset_ids),
            random.choice(tech_ids) if status != "Open" else None,
            wo_type,
            priority,
            status,
            random.choice(WO_DESCRIPTIONS[wo_type]),
            created_at.strftime("%Y-%m-%d %H:%M:%S"),
            started_at.strftime("%Y-%m-%d %H:%M:%S") if started_at else None,
            completed_at.strftime("%Y-%m-%d %H:%M:%S") if completed_at else None,
            downtime_hours,
            random.choice(FAILURE_CODES) if wo_type in ("Corrective", "Emergency") else None,
        ))
    return rows

def gen_costs(work_orders):
    rows = []
    for wo in work_orders:
        wo_id  = wo[0]
        status = wo[5]
        if status not in ("Completed", "In Progress"):
            continue
        # 1–3 cost lines per eligible WO
        for _ in range(random.randint(1, 3)):
            ctype = random.choices(
                ["Parts", "Labour", "Subcontract"], weights=[50, 40, 10]
            )[0]
            rows.append((
                wo_id,
                ctype,
                random.choice(COST_DESCRIPTIONS[ctype]),
                round(random.uniform(50, 4500) if ctype == "Parts"
                      else random.uniform(100, 2000) if ctype == "Labour"
                      else random.uniform(500, 8000), 2),
                "USD",
            ))
    return rows


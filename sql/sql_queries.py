SCHEMA = """
-- Assets being maintained (aircraft, GSE, vehicles, machinery)
CREATE TABLE IF NOT EXISTS assets (
    asset_id        TEXT PRIMARY KEY,
    asset_name      TEXT NOT NULL,
    asset_type      TEXT NOT NULL,       -- Aircraft, GSE, Vehicle, Machinery
    location        TEXT NOT NULL,       -- Hangar A, Hangar B, Apron, Workshop
    manufacturer    TEXT NOT NULL,
    model           TEXT NOT NULL,
    purchase_year   INTEGER NOT NULL,
    status          TEXT NOT NULL        -- Active, Inactive, Under Maintenance
);

-- Technicians who carry out work orders
CREATE TABLE IF NOT EXISTS technicians (
    technician_id   TEXT PRIMARY KEY,
    full_name       TEXT NOT NULL,
    skill_level     TEXT NOT NULL,       -- Junior, Senior, Lead
    specialisation  TEXT NOT NULL,       -- Avionics, Mechanical, Hydraulics, Electrical
    hourly_rate     REAL NOT NULL,
    active          INTEGER NOT NULL DEFAULT 1
);

-- SLA targets: how quickly each priority must be resolved
CREATE TABLE IF NOT EXISTS sla_definitions (
    priority        TEXT PRIMARY KEY,    -- Critical, High, Medium, Low
    target_hours    REAL NOT NULL,       -- Max hours to close after creation
    escalation_hours REAL NOT NULL       -- Hours before escalation warning
);

-- Core work order table
CREATE TABLE IF NOT EXISTS work_orders (
    wo_id               TEXT PRIMARY KEY,
    asset_id            TEXT NOT NULL REFERENCES assets(asset_id),
    technician_id       TEXT REFERENCES technicians(technician_id),
    wo_type             TEXT NOT NULL,   -- Preventive, Corrective, Inspection, Emergency
    priority            TEXT NOT NULL REFERENCES sla_definitions(priority),
    status              TEXT NOT NULL,   -- Open, In Progress, Completed, Cancelled
    description         TEXT NOT NULL,
    created_at          TEXT NOT NULL,
    started_at          TEXT,
    completed_at        TEXT,
    downtime_hours      REAL,            -- Asset downtime caused by this WO
    failure_code        TEXT             -- e.g. HYD-001, ELE-002
);

-- Parts and labour costs per work order
CREATE TABLE IF NOT EXISTS work_order_costs (
    cost_id         INTEGER PRIMARY KEY AUTOINCREMENT,
    wo_id           TEXT NOT NULL REFERENCES work_orders(wo_id),
    cost_type       TEXT NOT NULL,       -- Parts, Labour, Subcontract
    description     TEXT NOT NULL,
    amount          REAL NOT NULL,
    currency        TEXT NOT NULL DEFAULT 'USD'
);
"""


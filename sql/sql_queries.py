def build_database():
    conn = sqlite3.connect(DB_PATH)
    cur  = conn.cursor()
    cur.executescript(SCHEMA)

    # SLA definitions
    for priority, (target, escalation) in SLA_TARGETS.items():
        cur.execute(
            "INSERT OR IGNORE INTO sla_definitions VALUES (?,?,?)",
            (priority, target, escalation)
        )

    assets      = gen_assets(30)
    technicians = gen_technicians(20)
    work_orders = gen_work_orders(assets, technicians, 500)
    costs       = gen_costs(work_orders)

    cur.executemany("INSERT OR IGNORE INTO assets VALUES (?,?,?,?,?,?,?,?)", assets)
    cur.executemany("INSERT OR IGNORE INTO technicians VALUES (?,?,?,?,?,?)", technicians)
    cur.executemany("INSERT OR IGNORE INTO work_orders VALUES (?,?,?,?,?,?,?,?,?,?,?,?)", work_orders)
    cur.executemany(
        "INSERT INTO work_order_costs (wo_id,cost_type,description,amount,currency) VALUES (?,?,?,?,?)",
        costs
    )

    conn.commit()

    # ── Summary ──────────────────────────────
    print("✅  Database built:", DB_PATH)
    for table in ["assets", "technicians", "sla_definitions", "work_orders", "work_order_costs"]:
        count = cur.execute(f"SELECT COUNT(*) FROM {table}").fetchone()[0]
        print(f"   {table:<22} {count:>5} rows")

    # Quick sanity checks
    print("\n── SLA compliance preview ──")
    cur.execute("""
        SELECT
            wo.priority,
            COUNT(*) AS total,
            SUM(CASE
                WHEN (julianday(wo.completed_at) - julianday(wo.created_at)) * 24
                     <= sla.target_hours THEN 1 ELSE 0
            END) AS met,
            ROUND(
                100.0 * SUM(CASE
                    WHEN (julianday(wo.completed_at) - julianday(wo.created_at)) * 24
                         <= sla.target_hours THEN 1 ELSE 0
                END) / COUNT(*), 1
            ) AS pct
        FROM work_orders wo
        JOIN sla_definitions sla ON wo.priority = sla.priority
        WHERE wo.status = 'Completed'
        GROUP BY wo.priority
        ORDER BY CASE wo.priority
            WHEN 'Critical' THEN 1 WHEN 'High' THEN 2
            WHEN 'Medium' THEN 3 ELSE 4 END
    """)
    print(f"   {'Priority':<10} {'Total':>6} {'Met SLA':>8} {'%':>6}")
    for row in cur.fetchall():
        print(f"   {row[0]:<10} {row[1]:>6} {row[2]:>8} {row[3]:>5}%")

    print("\n── Total cost by type ──")
    cur.execute("""
        SELECT cost_type, COUNT(*), ROUND(SUM(amount),2)
        FROM work_order_costs GROUP BY cost_type
    """)
    for row in cur.fetchall():
        print(f"   {row[0]:<14} {row[1]:>4} lines   ${row[2]:>10,.2f}")

    conn.close()

if __name__ == "__main__":
    build_database()

